                        Por ello, el presente trabajo conllevó a que el grupo de docentes y estudiantes de la I. E. la Inmaculada, se reunieran a crear estrategias de prevención y formación para contribuir en la solución de las problemáticas planteadas.  De esta manera se crea  el grupo de teatro Caragabí, (nombre en honor a los Embera) conformados por 15 estudiantes de la Institución del grado 9°  y  edades  que oscilan entre los 14 y 16 años. Dentro de este grupo hacen parte integrantes de la población tanto  Embera como afro, favoreciendo con ello la inclusividad de estas poblaciones”. 
